namespace App
{
    /// <summary>
    /// Provides application-specific behavior to supplement the default Application class.
    /// </summary>
    public partial class App : Microsoft.UI.Xaml.Application
    {
        // Bewaart de hoofdvensterinstantie zodat andere pagina's ernaar kunnen verwijzen.
        private Window? _window;

        // Publieke referentie naar het hoofdvenster van de app.
        public MainWindow? MainWindow { get; private set; }

        /// <summary>
        /// Initializes the singleton application object.  This is the first line of authored code
        /// executed, and as such is the logical equivalent of main() or WinMain().
        /// </summary>
        public App()
        {
            InitializeComponent();
        }

        /// <summary>
        /// Invoked when the application is launched.
        /// </summary>
        /// <param name="args">Details about the launch request and process.</param>
        protected override void OnLaunched(Microsoft.UI.Xaml.LaunchActivatedEventArgs args)
        {
            // Maak het hoofdvenster aan en onthoud de instantie.
            MainWindow = new MainWindow();
            _window = MainWindow;

            // Activeer het venster om het zichtbaar te maken.
            MainWindow.Activate();
        }
    }
}
